class JCSPyGm_Math(object):

    """Math Library"""

    # --------------------------------------------
    # Public Variables
    # --------------------------------------------

    # --------------------------------------------
    # Private Variables
    # --------------------------------------------

    # --------------------------------------------
    # Protected Variables
    # --------------------------------------------

    # --------------------------------------------
    # Constructor
    # --------------------------------------------

    # --------------------------------------------
    # Public Methods
    # --------------------------------------------
    @staticmethod
    def abs(val):
        """Return absolute value."""
        
        if val < 0:
            return -val;
        
        return val
    
    @staticmethod
    def dot_product(vec1x, vec1y, vec2x, vec2y):
        """Do dot product."""
        
        return vec1x * vec2x + vec1y * vec2y

